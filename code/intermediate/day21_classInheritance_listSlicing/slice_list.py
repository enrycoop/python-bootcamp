piano_keys = ["a", "b", "c", "d", "e", "f", "g"]
piano_tuple = ("a", "b", "c", "d", "e", "f", "g")


print(piano_keys[2:5])

print(piano_keys[2:5:2])  # terzo numero, ogni quanto
print(piano_keys[::3])   # ['a', 'd', 'g']
print(piano_keys[::-1])  # lista al contrario


print(piano_tuple[2:5])

print(piano_tuple[2:5:2])  # terzo numero, ogni quanto
print(piano_tuple[::3])   # ['a', 'd', 'g']
print(piano_tuple[::-1])  # lista al contrario
